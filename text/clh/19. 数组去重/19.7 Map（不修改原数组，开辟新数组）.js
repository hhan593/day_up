// 定义一个数组，包含一些可能重复的数字
const arr = [1, 8, 3, 5, 5, 6, 9, 8, 9, 10];

/**
 * 从给定数组中移除重复的元素，保留每个元素的首次出现顺序
 * @param {Array} arr - 一个可能包含重复元素的数组
 * @returns {Array} - 一个不包含重复元素的数组
 */
function unique_seven(arr) {
  // 使用Map来记录每个元素是否已经出现过
  const map = new Map();
  // 初始化结果数组，用于存放不重复的元素
  const res = [];
  // 遍历输入数组
  for (var i = 0; i < arr.length; i++) {
    // 如果当前元素尚未在Map中出现过
    if (!map.has(arr[i])) {
      // 在Map中标记该元素已出现，下次再遇到相同的元素时，可以通过 `map.has(arr[i])` 检查该元素是否已经存在于 `Map` 中，从而避免重复添加到结果数组 `res` 中
      map.set(arr[i], true);
      // 将该元素添加到结果数组中
      res.push(arr[i]);
    }
  }
  // 返回不包含重复元素的数组
  return res;
}
console.log(unique_seven(arr));
/**
 * `map.set(arr[i], true);` 这行代码的作用是将数组中的元素作为键存储到 `Map` 对象中，并为每个键设置一个值 `true`。具体解释如下：

- **`map`**：这是一个 `Map` 对象，用于存储键值对。在这里，它用来记录数组中已经遇到的元素。
- **`arr[i]`**：这是数组 `arr` 中的第 `i` 个元素，作为 `Map` 的键。
- **`true`**：这是为每个键设置的值，在这里使用 `true` 只是为了标记该键（即数组中的元素）已经出现过。

### 详细说明：
- 当遍历数组时，如果某个元素第一次出现，就会通过 `map.set(arr[i], true)` 将其添加到 `Map` 中。
- 下次再遇到相同的元素时，可以通过 `map.has(arr[i])` 检查该元素是否已经存在于 `Map` 中，从而避免重复添加到结果数组 `res` 中。

### 总结：
这行代码的主要目的是利用 `Map` 来跟踪数组中已经处理过的元素，确保每个元素只被添加到结果数组一次，从而实现数组去重的功能。
 */
