//首先要明白作用域链的概念，其实很简单，在ES5中只存在两种作用域————全局作用域和函数作用域，
// `当访问一个变量时，解释器会首先在当前作用域查找标示符，如果没有找到，就去父作用域找，直到找到该变量的标示符或者不在父作用域中，这就是作用域链`，
// 值得注意的是，每一个子函数都会拷贝上级的作用域，形成一个作用域的链条。 比如:

// function f1() {
//   var a = 2;
//   function f2() {
//     console.log(a); //2
//   }
//   return f2;
// }
// var x = f1();
// x();

