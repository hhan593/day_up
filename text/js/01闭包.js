// }

//此时执行的时候将f5的作用域变为了 `f5拥有了window、f6和f6本身这几个作用域的访问权限，还是自底向上查找，在最近的f6找到了
//在这里是外面的变量`f5存在着父级作用域的引用`，因此产生了闭包，形式变了，本质没有改变
// f6();
// f5();
var a = 1;
function foo() {
  var a = 2;
  function baz() {
    // var a = 56;
    console.log(a);
  }
  bar(baz);
}
function bar(fn) {
  // 这就是闭包
  fn();
}
// 输出2，而不是1
